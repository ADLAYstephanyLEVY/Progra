public class sleep
{
    private int f, c;

    public sleep (int f, int c){
        this.f=f;
        this.c=c;
    }

    public int getF(){
        return f;
    }

    public int getC(){
        return c;
    }

    public void setF(int f){
        this.f=f;        
    }

    public void setC(int c){
        this.c=c;
    }     
}
